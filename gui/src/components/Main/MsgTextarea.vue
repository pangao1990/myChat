<template>
  <div class="textarea" contenteditable="true" @keydown="onKeydown" @cut.prevent @paste.prevent>
    <!-- {{ state.text }} -->
    <span v-for="item in state.textList">
      <span v-if="item.type == 'text'">{{ item.content }}</span>
      <highlightjs v-else :language="item.codeType" :code="item.content" class="textarea" style="margin-bottom: 0;" />
    </span>
  </div>
  <!-- <p v-html="state.markdown"></p> -->
</template>

<script setup>
import { reactive, watch } from 'vue'

const props = defineProps({
  text: {
    type: String,
    default: '',
    required: false
  }
})

const state = reactive({
  text: '',
  textList: []
})

watch(
  () => props.text,
  (text) => {
    // 奇数是文字，偶数是代码
    const textList = text.split('```')
    for (const index in textList) {
      let content = textList[index]
      if (index % 2 == 0) {
        // 偶数是文字
        state.textList.push({ 'type': 'text', 'content': content })
      } else {
        // 奇数是代码
        const codeTypeEnd = content.indexOf('\n')
        const codeType = content.slice(0, codeTypeEnd)
        content = content.slice(codeTypeEnd)
        state.textList.push({ 'type': 'code', 'codeType': codeType, 'content': content })
      }
    }
  },
  { immediate: true }
)

const onKeydown = (event) => {
  var e = event || window.event
  var keyCode = e.keyCode || e.which
  if ((e.ctrlKey || e.metaKey) && keyCode == 67) {
    // 允许键盘复制
    return true
  } else {
    // 禁止其他键盘操作
    e.preventDefault()
  }
}

</script>

